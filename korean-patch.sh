#!/bin/bash
# ============================================================
# insert_lines.sh
# 여러 파일의 특정 라인 또는 마지막 라인에 동일한 텍스트를 삽입하는 스크립트
# 사용법: bash insert_lines.sh
# ============================================================

set -euo pipefail

# ------------------------------------------------------------
# 삽입할 공통 내용 (여기만 수정)
# ------------------------------------------------------------
CONTENT=$(cat <<'EOF'
여기에 삽입할 내용을 작성하세요
두 번째 줄
세 번째 줄
EOF
)

# ------------------------------------------------------------
# 삽입 대상 목록 (파일경로:라인위치)
#   라인위치: 숫자 = 해당 줄 아래에 삽입
#             $   = 마지막 줄 아래에 삽입
# ------------------------------------------------------------
TARGETS=(
    "/path/to/file1.conf:$"
    "/path/to/file2.yaml:5"
    "/path/to/file3.conf:10"
)

# ------------------------------------------------------------
# 실행
# ------------------------------------------------------------
for target in "${TARGETS[@]}"; do
    file="${target%:*}"
    position="${target##*:}"

    if [[ ! -f "$file" ]]; then
        echo "[ERROR] 파일 없음: $file"
        continue
    fi

    cp "$file" "${file}.bak"

    if [[ "$position" == '$' ]]; then
        echo "$CONTENT" | sed -i "\$r /dev/stdin" "$file"
        echo "[OK] $file — 마지막 줄 아래에 삽입 완료"
    else
        echo "$CONTENT" | sed -i "${position}r /dev/stdin" "$file"
        echo "[OK] $file — ${position}번째 줄 아래에 삽입 완료"
    fi
done

echo "모든 작업 완료"